    private void loginbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbuttonActionPerformed
        // TODO add your handling code here:
         String workername = WorkerName.getText();
            String enteredPassword = new String(workerpassword.getPassword());

            try (Connection connection = DatabaseConnection.getConnection()) {
                String query = "SELECT * FROM worker WHERE name = ? AND pw = ?";
                try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
                    preparedStatement.setString(1, workername);
                    preparedStatement.setString(2, enteredPassword);
                    ResultSet resultSet = preparedStatement.executeQuery();

                    if (resultSet.next()) {
                        String status = resultSet.getString("status");
                        if (status.equals("APPROVED")) {
                            // Credentials are correct and worker is approved, open WorkerHome window
                            WorkerHome workerHome = new WorkerHome(workername,"worker");
                            workerHome.setVisible(true);
                            dispose(); // Close the current login window

                            // Show congratulatory message
                            JOptionPane.showMessageDialog(null, "Congratulations! You have successfully logged in.", "Login Successful", JOptionPane.INFORMATION_MESSAGE);
                        } else if (status.equals("PENDING")) {
                            // Worker's status is pending, display a message
                            JOptionPane.showMessageDialog(null, "Your account status is pending approval by the admin. Please wait for approval.", "Status Pending", JOptionPane.WARNING_MESSAGE);
                        }
                    } else {
                        // Credentials are incorrect, display error message
                        JOptionPane.showMessageDialog(null, "Incorrect workername or password. Please try again.", "Login Failed", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } catch (SQLException ex) {
                // Handle database connection errors
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Database connection error.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
